        VolControler vc = new VolControler();
         
        ArrayList<Vol> alu  = new ArrayList<Vol>();
        alu = (ArrayList<Vol>) vc.getAll();
 
        
        DefaultTableModel model = new DefaultTableModel() ;
        jTableVol.setModel(model) ; 
        model.addColumn("Id");
        model.addColumn("dateDepart");
        model.addColumn("heureDepart");
        model.addColumn("dateArrivee"); 
        model.addColumn("heureArrivee");
         model.addColumn("reservable");
          model.addColumn(" aeroport_depart_id");
           model.addColumn(" aeroport_arrivee_id");
        
        for(Vol v : alu){
            model.addRow( new Object[] 
            {   v.getId(),
                v.getdateDepart(), 
                v.getheureDepart(),                            
                v.getdateArrivee(),                        
                v.getheureArrivee(),
                v.getreservable(),
                v.getaeroport_depart_id(),
                 v.getaeroport_arrivee_id()
            });
        }
        System.out.println("Vol Not found");
        jTableVol.setModel(model);
 
